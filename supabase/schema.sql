  amount_paid numeric(12, 2) check (amount_paid is null or amount_paid >= 0),
  cash_received numeric(12, 2) check (cash_received is null or cash_received >= 0),
  change_due numeric(12, 2) check (change_due is null or change_due >= 0),
  payment_reference text,
  completed_at timestamptz,
  cancelled_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
