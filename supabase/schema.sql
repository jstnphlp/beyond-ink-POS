create or replace function public.decrement_inventory_item_stock(
  p_inventory_item_id uuid,
  p_quantity numeric,
  p_sales_transaction_id uuid default null,
  p_sales_material_entry_id uuid default null,
  p_note text default null
)
returns public.inventory_movements
language plpgsql
as $$
declare
  v_inventory_item public.inventory_items;
  v_movement public.inventory_movements;
begin
  if p_quantity is null or p_quantity <= 0 then
    raise exception 'Quantity to deduct must be greater than zero.';
  end if;

  update public.inventory_items
  set stock_quantity = stock_quantity - p_quantity
  where id = p_inventory_item_id
    and stock_quantity >= p_quantity
  returning * into v_inventory_item;

  if not found then
    raise exception 'Inventory item not found or insufficient stock.';
  end if;

  insert into public.inventory_movements (
    inventory_item_id,
    sales_transaction_id,
    sales_material_entry_id,
    movement_type,
    quantity_delta,
    note
  )
  values (
    p_inventory_item_id,
    p_sales_transaction_id,
    p_sales_material_entry_id,
    'sale_deduction',
    -p_quantity,
    p_note
  )
  returning * into v_movement;

  return v_movement;
end;
$$;

